    <h2>Mission</h2>
    
    <p>The Packwood Trail Project is a local non-profit organization founded by community members with a vision for development and maintenance of a multi-use, non-motorized system of trails.</p>

    <p>Our Mission is to develop and maintain a trail network in the Packwood area that (is):</p>
    <ul>
        <li>Non-motorized, built for hikers, runners, and mountain bikers</li>
        <li>Relatively easy to walk and ride</li>
        <li>Accessible year round, below the common winter snowline</li>
        <li>Pavement accessible at trailheads</li>
        <li>Provides an economic benefit to the Packwood area</li>
        <li>Brings together agencies, groups, and community </li>
        <li>Interprets the rich natural and cultural history of the area</li>
        <li>Link in with existing USFS system trails</li>
    </ul>
    
    <p>It is amazing what a small, determined community can accomplish. Imagine the possibilities here in the upper Cowlitz Valley!</p> 

    <img src="images/PTP-stickers-from-facebook.jpg" class="desktop" alt="PTP-stickers" />

    <h2>Need</h2>

    <p>The needs for such a network of trails are categorized as:</p>
    <ul>
        <li>Filling a void in recreational opportunities</li>
        <li>Building strong community</li>
        <li>Health and quality of life</li>
        <li>Economic growth</li>
        <li>Relationship building between government agencies, public organizations, and community</li>
    </ul>
    
    <p>There are more than 30 trails that are within 10 air miles of Packwood, however, these trails are neither accessible during the winter nor for much of the shoulder seasons. From November until April, there is a noticeable absence of outdoor experiences, trail running, hiking, and bike riding. This is neither by design nor by necessity.  Although downtown Packwood (elevation 1060 feet) does often get snow during the winter months, it rarely stays around for more than a week. The winter snowline usually starts at about 2000 feet.</p> 

    <p>Furthermore, trails that do exist near Packwood are too steep to ride up or for many, even to walk up. Most of the trails leave from the valley floor and climb several thousand feet within one or two miles.</p>

    <p>We suggest building a trail or system of trails in the elevation range between 1100 and 1600 feet with potential access points on Highway 12, Skate Creek Road (FR 52), Snyder Road, and Cannon Road. We want to see the trails with relatively little elevation gain and loss. A network of trails and trailheads with year-round access would serve as a boon to the residents and visitors of Packwood.</p>

