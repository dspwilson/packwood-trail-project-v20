 <h3>Where is Packwood?</h3>
    
    <p>Packwood is an unincorporated community located just south of Mount Rainier in easternmost Lewis County, Washington. Packwood is 90 miles west of Yakima and 75 miles east of Centralia on Highway 12. This is one of the more remote small cities in Western Washington and is encircled by the beautiful Gifford Pinchot National Forest.</p>  

    <p>Until the mid-2000's, Packwood was driven by a forest products economy with a lumber mill and a Forest Service work center. These two employers, plus a strong seasonally recreational community, provided the economic base for the town. Since the mill closed and the Forest Service closed the work center, the town of Packwood has struggled to find a new identity and to promote deeply rich recreational opportunities.</p>
    
